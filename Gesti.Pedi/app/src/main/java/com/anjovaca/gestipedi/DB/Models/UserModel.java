package com.anjovaca.gestipedi.DB.Models;

public class UserModel {
    int id;
    String dni, name, lastname, username, password, rol, city, country,  phone, email;

    public UserModel(int id, String dni, String name, String lastname, String username, String password, String rol, String phone, String email, String city, String country) {
        this.id = id;
        this.dni = dni;
        this.name = name;
        this.lastname = lastname;
        this.username = username;
        this.password = password;
        this.rol = rol;
        this.phone = phone;
        this.email = email;
        this.city = city;
        this.country = country;

    }

    public UserModel() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
